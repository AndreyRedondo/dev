# Lists goes in []
# List is a collection which is ordered and changeable. Allows duplicate members.
my_list = ["apple", "banana", "cherry"]

# add items to a list
my_list.append("orange")

# remove items from a list
my_list.remove("banana")

# move around a list
print(my_list)
for item in my_list:
      print(item)

